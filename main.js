
function openMenu () {
    let links = document.getElementById('downMenu');
    if(links===none) {
        links.display=== block ; 
    }
    else {
        links.display === none;
    }
}

const btnMenu = document.getElementById('menuBtn');
btnMenu.addEventListener('click',event => {
    event.preventDefault();
    function openMenu();
})






